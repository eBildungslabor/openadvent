<h2>Was ist ein Barcamp?</h2>

<p>Bei einer klassischen Konferenz steht das Programm vorab fest, es gibt referierende und teilnehmende Personen gibt und die Atmosphäre ist meist formalisiert. Diese Erwartungen werden bei einem Barcamp umgedreht: Das Programm steht nicht fest, sondern wird vor Ort gemeinsam entwickelt, alle sind teilgebende Personen und sind per ‚Du‘. Deshalb bezeichnet man Barcamps auch häufig als ‚Unkonferenzen‘.
<p>

<h2>Kann ein Barcamp funktionieren?</h2>

<p>Wer noch nie bei einem Barcamp war, der stellt oft in Frage, dass solch ein - auf den ersten Blick chaotischer - Ansatz funktionieren kann. Nach dem Besuch von inzwischen sehr vielen Barcamps kann ich beruhigen: es funktioniert großartig! Und insbesondere die Sessionplanung ist jedes Mal wie Weihnachten und Geburtstag zusammen. So viele spannende Themen und Beiträge, die angeboten werden.   

<p><h2>Welche Edu-Barcamps gibt es?</h2>

<ul class="alt">
<li>Das wahrscheinlich älteste Edu-Barcamp (auch die Mutter aller Barcamps genannt) im deutschsprachigen Raum ist das <a href="https://educamps.org/" target="blank">Educamp</a>, das jedes Jahr im Herbst in der DGB Bildungsstätte in Hattingen stattfindet. Das besondere an dem Educamp ist der generationenübergreifende Ansatz: Kinder und Jugendliche lernen selbstverständlich mit und bieten auch eigene Sessions an.
<li>Immer mehr Edu-Barcamps finden regional oder sogar als schulinterne Fortbildungsveranstaltung statt. Ein besonders gelungenes Barcamp war im letzten Jahr in Hamburg die <a href="http://www.edunautika.de/" target="blank">Edunautika</a> (von der es vielleicht eine Fortsetzung geben wird).  
<li>Das Barcamp der freien Bildungsmaterialien sind die <a href="https://www.oercamp.de/" target="blank">OERcamps</a>, die hoffentlich auch 2019 wieder stattfinden werden. 
</ul>

<p><h2>Welches Edu-Barcamp ist das beste für mich?</h2>
<p>Welches Barcamp Du besuchst, hängt von deinen Interessen und auch von Deinen Möglichkeiten in Hinblick auf Zeit und Ort ab. Doch letztendlich ist es egal für welches Barcamp Du Dich für den Einstieg entscheidest. Ich gehe fest davon aus, dass Du in jedem Fall begeistert sein wirst!
