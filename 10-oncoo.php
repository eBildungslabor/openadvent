<h2>💡 Einsatzmöglichkeiten im Unterricht</h2>

<p>
Grundsätzlich kann ONCOO genau so im Unterricht eingesetzt werden, wie man es mit der jeweils dahinter stehenden analogen Methode machen würde. Der Aufwand in Hinblick auf Material und Koordination ist aber deutlich geringer. Hier einige Beispiele für die Verwendung der Methoden:
<ul>
<li><strong>Kartenabfrage:</strong> Schülerinnen und Schüler sammeln Ideen zu einem bestimmten Thema/ einer Herausforderung. Diese werden als virtuelle Karten eingereicht. Zum Abschluss werden die Karten gemeinsam gesichtet und sortiert.
<li><strong>Zielscheibe:</strong> Die Zielscheibe kann zur Evaluation genutzt werden. Eingetragen werden Fragen zu Inhalt und/ oder Methodik einer Lerneinheit/ eines Projekts. Schülerinnen und Schüler treffen ihre Auswahl- Gemeinsam wird das Ergebnis angesehen und darüber reflektiert.</strong>
<li><strong>Placemat</strong>: Schülerinnen und Schüler bekommen unterschiedliche Texte/ Materialien zur Auseinandersetzung. Nach einer ersten Beschägtigung mit ihrem Material, werden sie mit Schülerinnen und Schüler zusammen gebracht, die sich mit einem anderen Material beschäftigt haben. </strong>

<p><h2>➕ Answergarden: ein weiteres Tool speziell für Feedback </h2>
<p>Wer in ähnlicher Form wie bei ONCOO, d.h. mit einer zentralen Präsentation und Beteiligung der Lernenden mit ihren eigenen Geräten, Feedback einholen will, kann dazu <a href=https://answergarden.ch>Answergarden</a> nutzen. Auch hier kann man direkt eine Frage eingeben und den Lernenden den Link weiter geben. Eine Anmeldung ist nicht erforderlich.
 

