<p>										<h2>1. Aufträge basierend auf einem Geschäftsmodell des Teilens</h2>
								
Die Inhalte von Open.Education basieren auf meinen Erfahrungen und meinem Wissen, das ich mir insbesondere im Rahmen von bezahlten Aufträgen erarbeitet habe. Unter anderem habe ich 2018 gearbeitet für das Forum Bildung Digitalisierung, für Engagement Global, für die Kooperative Berlin und für mehrere Landesfortbildungsinstitute und Nichtregierungsorganisationen. Herzlichen Dank für die gute Kooperation und schöne Zusammenarbeit!
<p>Ich kann Open.Education fortsetzen, wenn ich auch weiterhin Aufträge erhalten, die Raum lassen für mein eigenes Lernen und die das Teilen der Erfahrungen und Ergebnisse für alle als Bestandteil des Auftrags sehen. Ich nenne dies ein Geschäftsmodell des Teilens. Der jeweilige Auftraggeber hat keine Nachteile, da er die beauftragten Leistungen erhält - zugleich ermöglicht er, dass mehr Menschen davon profitieren.
