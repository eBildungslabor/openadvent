<h2>🔧 Tool-Empfehlung</h2>

<p>Pechaflickr gibt es schon seit langem in einer <a href=http://pechaflickr.net/>englischsprachigen Version</a>. Da der Code Open Source ist, konnte ich daraus eine deutschsprachige Version unter  <a href="https://pechaflickr.de">pechaflickr.de</a> erstellen und sie für den Einsatz im schulischen Kontext anpassen. Um Pechaflickr im Unterricht zu nutzen, musst Du nichts vorbereiten, sondern nur die Website öffnen (Wenn Du Pechaflickr frontal mit der ganzen Klasse spielen willst, benötigst Du eine Präsentationsmöglichkeit.)
<ul><a href="https://pechaflickr.de" class="button primary small">Pechaflickr-Website</a>
												
											</ul>
Du kannst Pechaflickr auf zwei Arten spielen: 

<ol>
<li>mit bekanntem Schlagwort: Die Schülerinnen und Schüler kennen das Schlagwort - und bereiten sich auf eine Präsentation dazu vor. 
<li>mit unbekanntem Schlagwort: Die Schülerinnen und Schüler bekommen Bilder präsentiert und müssen raten, welches das verbindende Schlagwort ist.
</ul>
