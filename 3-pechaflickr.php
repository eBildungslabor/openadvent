<p><h2>💡 Einsatzmöglichkeiten im Unterricht</h2>

<ul class="alt">
<li>Pechaflickr ist insbesondere im Fremdsprachenunterricht hilfreich, um freies Sprechen zu trainieren. ZU Beginn des Fremdsprachenlernens kann man Pechaflickr auch zum Vokabel-Training verwenden: Sage bzw. schreibe so viele Wörter, wie möglich zum angezeigten Bild. </li> 
<li>Pechaflickr kann in praktisch jedem Fach als spielerischer Abschluss einer bestimmten Unterrichtseinheit genutzt werden. Die Schülerinnen und Schüler üben sich damit im freien und spontanem Sprechen.</li>
<li>Pechaflickr ist geeignet für Einzel- oder Partner/innen-Arbeit: Jeder und jede startet ein Pechaflickr mit dem gleichen Schlagwort und muss mindestens einen Satz zu jedem Bild aufschreiben. Oder man spricht sich zu zweit gegenseitig ein Pechaflickr vor.
<li>Pechaflickr kann auch als Gruppenarbeit gestaltet werden: Zwei oder mehr Teams treten gegeneinander an. Immer ein Schüler/ eine Schülerin übernimmt dabei ein Bild. Wer hält das beste Pechaflickr? 
<li>Pechaflickr kann auch eine Herausforderung für Dich als Lehrer/in sein: Die Schülerinnen und Schüler dürfen sich ein Schlagwort wählen. Du hältst das Pechaflickr dazu.
</ul>
<p>Das Spielen mit Pechaflickr ist auch eine gute Vorbereitung, um eigene Pechakuchas zu erstellen und zu präsentieren. Dazu erwarten Dich in diesem Adventskalender noch weitere Ideen!  
