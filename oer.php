<h2>Was sind OER?</h2>

<p>OER steht für Open Educational Resources. Es handelt sich dabei um Bildungsmaterialien, die unter einer offenen Lizenz veröffentlicht sind und vor diesem Hintergrund weiter verwendet, verändert und verbreitet werden dürfen. Das unterscheidet sie von proprietären Materialien, die in den engen Schranken des Urheberrechts gefangen sind. Sich in OER einzuarbeiten ist kein Hexenwerk. In 5 Minuten hast Du z.B. <a href=https://www.ebildungslabor.de/blog/oereinstieg target="blank">diesen von mir im eBildungslabor veröffentlichten Blogbeitrag</a> zum Thema gelesen - und weißt danach über die wichtigsten Grundlagen Bescheid. Das weitere Lernen kommt dann mit der Praxis! 
<p>

<h2>Wo finde ich von anderen erstellte OER?</h2>

<p>Nach OER-Materialien suchst Du am besten beim <a href="https://oerhoernchen.de/suche" target="blank">OERhörnchen</a>: ein ganz besonderes Eichhörnchen, das nicht nur gerne sammelt, sondern auch gerne teilt. Verlinkt sind hier alle deutschsprachigen OER-Projekte. Du kannst einen beliebigen Suchbegriff eingeben - und erhältst dazu ein Google-Suchergebnis; beschränkt auf die OER-Webseiten.

<p><h2>Wie kann ich meine selbst erstellten OER veröffentlichen?</h2>

<p>Es gibt nicht eine zentrale Plattform zum Upload von OER. Die meisten OER-Projekte freuen sich aber über Mitarbeit. Oft ist es dort möglich, eigene Materialien beizusteuern. Viele veröffentlichen ihre OER auch im Rahmen eines eigenen Blogs und weisen via Social Media darauf hin (bei Twitter gibt es hierzu den Hashtag #OERde). Alternativ oder zusätzlich kannst Du auch die <a href="https://bildungsmaterialspende.de" target="blank">Bildungsmaterialspende</a> nutzen. Diese bietet Dir einen Service der rechtlichen Prüfung, der Systematisierung mit Metatags und der Veröffentlichung.

<p><h2>Wie hängen OER und Vernetzung zusammen?</h2>
<p>Der Austausch von Bildungsmaterialien und das gemeinsame Arbeiten an ihrer Weiterentwicklung ist eine wunderbare Möglichkeit für einen produktiven Austausch und eine daraus folgende Vernetzung mit anderen Bildungsakteuren. Längst ist man dabei nicht nur auf das eigene Kollegium begrenzt, sondern kann Materialien schulübergreifend und sogar grenzüberschreitend weiter entwickeln. 


<p>Übrigens: Immer aktuell über OER informiert, wirst Du von der bundesweiten <a href="https://open-educational-resources.de/" target="blank">Informationsstelle Open Educational Resources</a>.

