<h2>Warum sollte ich Twitter nutzen?</h2>

<p>Twitter ist für mich mit die wichtigste Inspirationsquelle zu Bildungsthemen. Nirgendwo sonst erhalte ich so komprimiert Einblicke in aktuelle Entwicklungen, Ideen zum Ausprobieren oder Ankündigungen. Darüber hinaus kann ich Twitter auch für eigene Fragen nutzen: Wer weiß Bescheid über ein bestimmtes Tool? Wer kann bei einer Veranstaltung unterstützen? Wer hat eine Idee für eine Methode? Und: Natürlich kann ich Twitter auch nutzen, um meine eigfenen Ideen und Erfahrungen mit anderen zu teilen.
<p>

<h2>Wie fange ich am besten an?</h2>

<p>Passiv mitlesen kannst Du bei Twitter auch ohne einen eigenen Account. Um Vernetzung zu erreichen, solltest Du Dich aber registrieren. Für Dein Profil ist eine kurze Beschreibung und ein Profilbild wichtig, Danach kannst Du schon Deinen ersten Tweet schreiben - und suchen, wem Du folgen möchtest. Beim Online-Kurs 'DreiWot - 3 Wochen Twitter' sind die <a href="https://blogs.uni-bremen.de/3wot/erstelle-ein-twitter-profil/" target="blank">ersten Schritte auf Twitter</a> wunderbar erklärt. 

<p><h2>Wer hilft mir beim Einstieg?</h2>

<p>Die Twitter-Bildungscommunity ist grundsätzlich sehr offen und hilfsbereit. Frage einfach nach, wenn Dir etwas  unklar ist. Wenn Du jemanden gezielt ansprechen willst, kannst Du ihn mit @nutzername antwittern. Er wird dann in der Regel eine Benachrichtigung erhalten und Deinen Tweet finden. 
<p>Um Dir den Einstieg auf Twitter zu erleichtern, haben sich einige Pat/innen (inklusive mir selbst) zur Verfügung gestellt. Wenn Du Unterstützung brauchst, dann suche Dir jemanden von ihnen aus und twittere sie an. Sie können Dir auch dabei helfen, Dich in der Twitter-Community bekannt zu machen und erste Follower zu finden.
