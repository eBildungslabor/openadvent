<p><h2>Ich bin jetzt auf Twitter. Was mache ich da?</h2>
<p>Twitter funktioniert vor allem auch über Hashtags. Diese sind auch ein guter Einstieg, um Gleichgesinnte bei Twitter zu finden. Als Hashtags empfehle ich: #twitterlehrerzimmer, #zeitgemäßebildung oder #OERde.
<p>Außerdem empfehle ich Dir, Dich an einem Twitterchat zu beteiligen. Das ist ein meist einstündiger Austausch, der rein über Twitter abläuft. Man muss sich nirgends dafür anmelden, sondern nur zur vereinbarten Zeit die Tweets des Hashtags lesen und eigene Tweets mit dem Hashtag formulieren. Fachübergreifend ist z.B. der #Edchatde etabliert, der außer in Ferienzeiten immer Dienstags um 20 Uhr stattfindet. Daneben gibt es auch fachspezifische Twitterchats wie z.B. den #relichat.  

